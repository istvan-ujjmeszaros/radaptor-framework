	/**
	 * Find all role mappings for a usergroup.
	 *
	 * @param int $usergroupId
	 * @return array<static>
	 */
	public static function findByUsergroupId(int $usergroupId): array
	{
		return static::findMany(['usergroup_id' => $usergroupId]);
	}

